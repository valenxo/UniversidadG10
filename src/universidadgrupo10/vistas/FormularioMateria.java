    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        try {
        Integer codigo=Integer.parseInt(textCodigo.getText());
        matActual=matData.buscarMateria(codigo);
        if(matActual != null ){
            textNombre.setText(matActual.getNombre());
            textAño.setText(""+matActual.getAño());
            checkEstado.setSelected(matActual.getEstado());
        }
        
        
        }catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(this, "Deber ingresar un numero valido");
        }
    }//GEN-LAST:event_botonBuscarActionPerformed

