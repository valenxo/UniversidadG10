    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        
        try {
            Integer codigo=Integer.parseInt(textCodigo.getText());
            String nombre=textNombre.getText();
            Integer año=Integer.parseInt(textAño.getText());
            
            if(codigo == 0){
                
                JOptionPane.showMessageDialog(this,"Los campos no pueden estar vacios");
                return;
            }
            Boolean estado=checkEstado.isSelected();
            if(matActual==null){
                
                matActual=new Materia(codigo,nombre,año,estado);
                matData.cargarMateria(matActual);
                
            }else{
                
                matActual.setIdMateria(codigo);
                matActual.setNombre(nombre);
                matActual.setAño(año);
                matData.modificarMateria(matActual);
            }

        }catch(NumberFormatException nfc){
            JOptionPane.showMessageDialog(this, "Por favor, introducir numeros validos en los campos");
        }
        
        
        
    }//GEN-LAST:event_botonGuardarActionPerformed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        
        if(matActual !=null){
            matData.eliminarMateria(matActual.getIdMateria());
            matActual=null;
            limpiarCampos();
        }else{
            
            JOptionPane.showMessageDialog(this, "Materia Inexistente");
        }

    }//GEN-LAST:event_botonEliminarActionPerformed

